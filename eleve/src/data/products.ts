const ensureUniqueProductImages = (product: Product): Product => {
  const original = product.images ?? [];
  const unique: string[] = [];
  const seen = new Set<string>();

  for (const url of original) {
    const trimmed = (url ?? "").trim();
    if (!trimmed) continue;
    if (seen.has(trimmed)) continue;
    seen.add(trimmed);
    unique.push(trimmed);
  }

  const targetCount = Math.max(original.length, 3);

  let i = 1;
  while (unique.length < targetCount) {
    const seedUrl = `https://picsum.photos/seed/eleve-${product.id}-${i}/800/1000`;
    if (!seen.has(seedUrl)) {
      seen.add(seedUrl);
      unique.push(seedUrl);
    }
    i += 1;
    if (i > 20) break;
  }

  return { ...product, images: unique };
};

export const products: Product[] = rawProducts.map(ensureUniqueProductImages);

export const getProductById = (id: number): Product | undefined => {
  return products.find((product) => product.id === id);
};

export const getProductsByGender = (gender: Gender): Product[] => {
  return products.filter((product) => product.gender === gender);
};
